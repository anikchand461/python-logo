from turtle import *

s = Screen()
s.bgcolor('black')
hideturtle()
speed('fast')

#upper part
def draw_upper():
    pencolor((49 / 255, 119 / 255, 177 / 255))
    up()
    goto(-100, 119)
    seth(90)
    down()
    begin_fill()
    fillcolor((49 / 255, 119 / 255, 177 / 255))
    fd(26)
    circle(-45, 70)
    circle(-200, 40)
    circle(-45, 70)
    seth(-90)
    fd(95)
    circle(-48, 80)
    seth(180)
    fd(120)
    circle(48, 80)
    seth(-90)
    fd(60)
    seth(180)
    fd(40)
    circle(-45, 70)
    circle(-200, 42)
    circle(-45, 70)
    seth(0)
    fd(130)
    lt(90)
    fd(10)
    lt(90)
    fd(83)
    end_fill()

#snake eye-upper
def upper_eye():
    up()
    pencolor('black')
    begin_fill()
    fillcolor('black')
    goto(-70, 135)
    down()
    circle(-15)
    end_fill()

#lower part
def draw_lower():
    pencolor((255 / 255, 213 / 255, 55 / 255))
    up()
    goto(100, -119)
    seth(-90)
    down()
    begin_fill()
    fillcolor((255 / 255, 213 / 255, 55 / 255))
    fd(26)
    circle(-45, 70)
    circle(-200, 40)
    circle(-45, 70)
    seth(90)
    fd(90)
    circle(-48, 80)
    seth(0)
    fd(120)
    circle(48, 80)
    seth(90)
    fd(60)
    seth(0)
    fd(40)
    circle(-45, 70)
    circle(-200, 42)
    circle(-45, 70)
    seth(180)
    fd(130)
    lt(90)
    fd(10)
    lt(90)
    fd(83)
    end_fill()

#snake eye-lower
def lower_eye():
    up()
    pencolor('black')
    begin_fill()
    fillcolor('black')
    goto(70, -135)
    down()
    circle(-15)
    end_fill()

draw_upper()
upper_eye()
draw_lower()
lower_eye()

s.mainloop()
